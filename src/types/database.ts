export interface ITransaction {
  id: string;          // UUID Primary Key
  customer_id: string; // UUID Foreign Key
  company_id: string;  // UUID Foreign Key (used by your triggers)
  amount: number;      // DECIMAL(12, 2)
  created_at: string | Date;
}

/**
 * For adding new transactions, we only need these three fields.
 * Everything else (id, created_at) is handled by the DB.
 */
export type CreateTransactionInput = Omit<ITransaction, 'id' | 'created_at'>;


