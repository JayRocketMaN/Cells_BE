// 1. Enum Definitions (Must match your Postgres ENUMs exactly)
export type SpendingLevel = 'Low' | 'Medium' | 'High';
export type LoyaltyTier = 'Tier 1' | 'Tier 2' | 'Tier 3';
export type CustomerStatus = 'Bronze' | 'Silver' | 'Gold' | 'Platinum';
export type Gender = 'Male' | 'Female' | 'Other' | 'Prefer Not to Say';

// 2. The Main Interface
export interface ICustomer {
  // Primary Keys & Identifiers
  id: string; // UUID
  internal_serial: number; // BIGINT (Handled by DB)
  display_id: string; // VARCHAR (Generated by your 'generate_public_customer_id' function)
  company_id: string; // UUID (Foreign Key)

  // Personal Info
  full_name: string;
  phone_number: string | null;
  email: string;
  date_of_birth: string | Date | null; // DATE
  gender: Gender | null;
  preferred_location: string | null;
  favorite_orders: string[] | null; // TEXT[]
  notes: string | null;

  // Financials (Calculated by 'sync_customer_financials' trigger)
  total_spent: number; // DECIMAL(12, 2)
  visits_count: number; // INTEGER
  average_spend: number; // GENERATED ALWAYS AS...

  // Status & Logic
  spending_level: SpendingLevel;
  loyalty_tier: LoyaltyTier;
  customer_status: CustomerStatus;
  feedback_score: number; // DECIMAL(3, 2)

  // Dates
  last_visit: string | Date | null;
  created_at: string | Date; // TIMESTAMP WITH TIME ZONE
}

// 3. Helper Type for Creating a Customer
// Use this when inserting data so you don't accidentally try to provide 
// fields that the Database handles automatically (like display_id or total_spent)
export type CreateCustomerInput = Omit<
  ICustomer, 
  'id' | 'internal_serial' | 'display_id' | 'average_spend' | 'total_spent' | 'visits_count' | 'created_at'
>;


